    //ID_USUARIO_MANUTENCAO
    private void salvar() {
        if (!ControleUniversal.obrigatorio(txtNome)) {
            return;
        }
        this.data = new HashMap<>();
        this.data.put("NOME", txtNome.getText());
        this.data.put("TELEFONE", txtTelefone.getText());
        if (ControleEmpresa.crud(data, txtCodigo.getText().equals("") ? BriefDb.INSERT : BriefDb.UPDATE, txtCodigo.getText())) {
            this.dispose();
            Alert.info("Os dados da empresa foram atualizados! =)", "SUCCESS");
        }
    }
